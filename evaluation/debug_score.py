"""
Utility script for debugging the quadgram scoring logic.
Provides detailed, step-by-step output of how the engine calculates
log-probabilities for a given word across both English and Hebrew models.
"""
import sys
import os
import math

# Add the project root to sys.path so we can import 'core'
sys.path.append(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

from core.quadgram import QuadgramModel
from core.keymap import shadow

def verbose_score(model, text, label):
    """
    Computes and prints a detailed quadgram score analysis for a given text.
    
    This function breaks down the scoring process step-by-step, showing the
    base bigram score followed by each subsequent quadgram step. It outputs
    the exact counts, vocabulary scaling, and cumulative probabilities.
    
    Args:
        model (QuadgramModel): The language model to use for scoring.
        text (str): The input string to score (usually padded with spaces).
        label (str): A descriptive label for the print output.
        
    Returns:
        float: The final cumulative log-probability score.
    """
    print(f"\n--- SCOING ANALYSIS: {label} [{text!r}] ---")
    if len(text) < 2:
        print("Text too short to score.")
        return 0.0

    text = text.lower()
    v = model.vocab_size
    total_log_prob = 0.0

    # 1. Base Score (First Bigram)
    first_bigram = text[:2]
    bi_comp_count = model.bigram_counts.get(first_bigram, 0)
    denominator = model.total_bigrams + (v ** 2)
    step_prob = (bi_comp_count + 1) / denominator
    step_log = math.log(step_prob)
    total_log_prob = step_log

    print(f"[BIGRAM START] {first_bigram!r}")
    print(f"  Count: {bi_comp_count:,}")
    print(f"  Total: {denominator:,}")
    print(f"  Log-Prob: {step_log:.4f}")

    # 2. Quadgram Loop
    for i in range(len(text) - 3):
        quadgram = text[i:i + 4]
        trigram = text[i:i + 3]

        quad_count = model.quadgram_counts.get(quadgram, 0)
        tri_count = model.trigram_counts.get(trigram, 0)

        step_prob = (quad_count + 1) / (tri_count + v)
        step_log = math.log(step_prob)
        total_log_prob += step_log

        print(f"[QUADGRAM STEP] {quadgram!r} | Tri: {trigram!r}")
        print(f"  Quad Count: {quad_count:,}")
        print(f"  Tri Count: {tri_count:,}")
        print(f"  V-Scale: {v}")
        print(f"  Step Log-Prob: {step_log:.4f}")
        print(f"  Cumulative Score: {total_log_prob:.4f}")

    print(f"FINAL SCORE ({label}): {total_log_prob:.4f}")
    return total_log_prob

def analyze_word(word_en, en_model, he_model):
    """
    Analyzes how the evaluation engine interprets a word depending on the layout.
    
    It simulates typing the word while the English layout is active,
    and then simulates typing the same physical keys while the Hebrew layout 
    is active. It compares the scores generated by both language models to
    demonstrate whether the engine would trigger a layout switch.
    
    Args:
        word_en (str): The word to analyze, typed as in an English layout.
        en_model (QuadgramModel): The initialized English language model.
        he_model (QuadgramModel): The initialized Hebrew language model.
    """
    word_he = shadow(word_en, direction='en_to_he')
    
    # Prepend/Append space to mimic EvaluationEngine word context
    eval_en = ' ' + word_en + ' '
    eval_he = ' ' + word_he + ' '

    print(f"\n{'='*50}")
    print(f" ANALYZING WORD: {word_en!r} <-> {word_he!r} ")
    print(f"{'='*50}")

    # If I'm in English, I type "yo" (eval_en). The engine compares:
    # 1. EN_model.score(eval_en)  [s_active]
    # 2. HE_model.score(eval_he)  [s_shadow]
    
    s_en_en = verbose_score(en_model, eval_en, "ENGLISH MODEL [ACTIVE: EN]")
    s_he_he_shadow = verbose_score(he_model, eval_he, "HEBREW MODEL [SHADOW: HE]")
    
    # If I'm in Hebrew, I type "yo" (getting "טם" = eval_he). The engine compares:
    # 1. HE_model.score(eval_he)  [s_active]
    # 2. EN_model.score(eval_en)  [s_shadow]
    
    s_he_he_active = verbose_score(he_model, eval_he, "HEBREW MODEL [ACTIVE: HE]")
    s_en_en_shadow = verbose_score(en_model, eval_en, "ENGLISH MODEL [SHADOW: EN]")

    print(f"\n{'='*50}")
    print(f" COMPARISON SUMMARY ")
    print(f"{'='*50}")
    
    diff_if_en = s_he_he_shadow - s_en_en
    print(f"Currently in EN: Active_EN={s_en_en:.4f} | Shadow_HE={s_he_he_shadow:.4f}")
    print(f"  -> Score Difference: {diff_if_en:+.4f}")
    print(f"  -> Switch to HE? {'YES' if diff_if_en > 0.0 else 'NO'}")

    diff_if_he = s_en_en_shadow - s_he_he_active
    print(f"\nCurrently in HE: Active_HE={s_he_he_active:.4f} | Shadow_EN={s_en_en_shadow:.4f}")
    print(f"  -> Score Difference: {diff_if_he:+.4f}")
    print(f"  -> Switch to EN? {'YES' if diff_if_he > 0.0 else 'NO'}")

if __name__ == "__main__":
    test_word = "ht"
    if len(sys.argv) > 1:
        test_word = sys.argv[1]

    data_dir = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), 'data')
    en_path = os.path.join(data_dir, 'en_quadgrams.json')
    he_path = os.path.join(data_dir, 'he_quadgrams.json')

    print("Loading models...")
    en_model = QuadgramModel(en_path)
    he_model = QuadgramModel(he_path)

    analyze_word(test_word, en_model, he_model)
